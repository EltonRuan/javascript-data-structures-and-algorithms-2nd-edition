// Função que imprime "Olá mundo" no console
function olaMundo() {
    console.log('Olá mundo');
}

// Chamando a função para executá-la
olaMundo();

// Função que imprime um texto recebido como argumento
function escrita(texto) {
    console.log(texto);
}

// Chamando a função com um argumento
escrita('Olá mundo');

// Função que recebe dois números como argumentos e retorna a soma deles
function soma(num1, num2) {
    return num1 + num2;
}

// Chamando a função soma e armazenando o resultado em uma variável
var resultado = soma(1, 2);

// Imprimindo o resultado no console
console.log(resultado); // retorna 3
