// Subtração
var resultadoSubtracao = 10 - 7;
console.log("Subtração:", resultadoSubtracao);

// Multiplicação
var resultadoMultiplicacao = 4 * 6;
console.log("Multiplicação:", resultadoMultiplicacao);

// Divisão
var resultadoDivisao = 20 / 5;
console.log("Divisão:", resultadoDivisao);

// Módulo (resto de uma divisão)
var resultadoModulo = 17 % 4;
console.log("Módulo:", resultadoModulo);

// Incremento (adição de 1)
var numero = 10;
numero++;
console.log("Incremento:", numero);

// Decremento (subtração de 1)
numero--;
numero--;
console.log("Decremento:", numero);

// Operadores de atribuição

var X = 10;
var Y = 5;

// Atribuição
var atribuicao = X;
console.log("Atribuição:", atribuicao);

// Atribuição de soma
X += Y;
console.log("Atribuição de soma:", X);

// Atribuição de subtração
X -= Y;
console.log("Atribuição de subtração:", X);

// Atribuição de multiplicação
X *= Y;
console.log("Atribuição de multiplicação:", X);

// Atribuição de divisão
X /= Y;
console.log("Atribuição de divisão:", X);

// Atribuição de resto
X %= Y;
console.log("Atribuição de resto:", X);

// Operadores de comparação

var a = 5;
var b = 10;

// Igual a
console.log("Igual a:", a == b);

// Igual a (valor e tipo)
console.log("Igual a (valor e tipo):", a === b);

// Diferente de
console.log("Diferente de:", a != b);

// Maior que
console.log("Maior que:", a > b);

// Maior ou igual a
console.log("Maior ou igual a:", a >= b);

// Menor que
console.log("Menor que:", a < b);

// Menor ou igual a
console.log("Menor ou igual a:", a <= b);

// Operadores lógicos

var c = true;
var d = false;

// E
console.log("E:", c && d);

// Ou
console.log("Ou:", c || d);

// Negação / Diferente
console.log("Negação / Diferente:", !c);

// Suporte para Operadores bit a bit

var e = 5; // 101
var f = 3; // 011

// E bit a bit
console.log("E bit a bit:", e & f);

// Ou bit a bit
console.log("Ou bit a bit:", e | f);

// Negação bit a bit
console.log("Negação bit a bit:", ~e);

// Ou exclusivo bit a bit
console.log("Ou exclusivo bit a bit:", e ^ f);

// Deslocamento para a esquerda
console.log("Deslocamento para a esquerda:", e << 1);

// Deslocamento para a direita
console.log("Deslocamento para a direita:", e >> 1);

